def викторина():
    вопросы = {
        "А.С. Пушкин": 1799,
        "Л.Н. Толстой": 1828,
        "Ф.М. Достоевский": 1821,
        "И.С. Тургенев": 1818,
        "А.П. Чехов": 1860
    }

    правильные_ответы = 0
    всего_вопросов = len(вопросы)

    for человек, год in вопросы.items():
        ответ = input(f"Введите год рождения {человек}: ")
        if ответ == str(год):
            print("Верно")
            правильные_ответы += 1
        else:
            print(f"Неверно, правильный год: {год}")

    ошибки = всего_вопросов - правильные_ответы
    процент_правильных = (правильные_ответы / всего_вопросов) * 100
    процент_неправильных = 100 - процент_правильных

    print(f"\nКоличество правильных ответов: {правильные_ответы}")
    print(f"Количество ошибок: {ошибки}")
    print(f"Процент правильных ответов: {процент_правильных:.2f}%")
    print(f"Процент неправильных ответов: {процент_неправильных:.2f}%")

    снова = input("Хотите начать игру сначала? (да/нет): ")
    if снова.lower() == "да":
        викторина()
    else:
        print("Спасибо за игру!")


викторина()